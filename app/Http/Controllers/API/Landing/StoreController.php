    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request) {
        $stores = Store::query()
            ->when(
                $request->boolean('active_only', false),
                fn($q) => $q->where('is_active', true)
            )
            ->orderBy('name')
            ->get();

        return response()->json([
            'success' => true,
            'data' => $stores
        ]);
    }

